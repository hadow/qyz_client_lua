local PlotDefine    = require("plot.base.plotdefine");
local ChapterUI = {};
-----------------------------------------------------------------------------------------------------------------------------------
ChapterUI.LoadFunction = nil;
-----------------------------------------------------------------------------------------------------------------------------------

------------------------------------------------------------------------------------------------------------------------------------

ChapterUI.StartFunction = function(self)
    self.Cutscene.m_UI:ShowChapterUI({ fieldList = self.FieldList })
    
    self.CurrentState = PlotDefine.ElementState.Started;
end

-----------------------------------------------------------------------------------------------------------------------------------
ChapterUI.EndFunction = function(self,container,cutscene)
    self.Cutscene.m_UI:HideChapterUI()
	self.CurrentState = PlotDefine.ElementState.Ended;
end
-----------------------------------------------------------------------------------------------------------------------------------
ChapterUI.DestroyFunction = nil;
-----------------------------------------------------------------------------------------------------------------------------------
ChapterUI.SampleFunction = nil;
-----------------------------------------------------------------------------------------------------------------------------------
return ChapterUI;
