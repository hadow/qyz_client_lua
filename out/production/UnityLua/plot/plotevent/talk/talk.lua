local PlotDefine    = require("plot.base.plotdefine");
local DialogSystem	= require("plot.plotdialog")
local Talk = {};
-----------------------------------------------------------------------------------------------------------------------------------
Talk.LoadFunction = nil;
-----------------------------------------------------------------------------------------------------------------------------------

------------------------------------------------------------------------------------------------------------------------------------

Talk.StartFunction = function(self)
        
    self.Cutscene.m_Dialog:Show(self.DialogSystem, {m_TalkId = self.TalkId, m_TalkContent = self.TalkContent})

    self.CurrentState = PlotDefine.ElementState.Started;
end
-----------------------------------------------------------------------------------------------------------------------------------

-----------------------------------------------------------------------------------------------------------------------------------
Talk.EndFunction = function(self,container,cutscene)
    self.Cutscene.m_Dialog:Hide(self.DialogSystem, {})
    
	self.CurrentState = PlotDefine.ElementState.Ended;
end
-----------------------------------------------------------------------------------------------------------------------------------
Talk.DestroyFunction = nil;
-----------------------------------------------------------------------------------------------------------------------------------
Talk.SampleFunction = nil;
-----------------------------------------------------------------------------------------------------------------------------------
return Talk;
